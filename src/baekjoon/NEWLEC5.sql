SELECT TO_CHAR(12345678,'$99,999,999,999.99') FROM DUAL;
SELECT 
	TRIM(TO_CHAR(1234567,'9,999,999'))||'원'
	,TO_CHAR(1234567.34567,'9,999,999.99')||'원' 
FROM DUAL;

SELECT TO_CHAR(SYSDATE,'YYYY/MM/DD PM HH24:MI:SS'),SYSDATE FROM DUAL;
SELECT TO_DATE('2014-03-31 12:23:03','YYYY/MM/DD HH:MI:SS') FROM DUAL;
SELECT TO_NUMBER('1994') FROM DUAL;